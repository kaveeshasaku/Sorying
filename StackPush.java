package DataStructures;

import java.util.Stack;

public class StackPush {
	
		public static void main(String[] args)
		{

			
			Stack<String> stack = new Stack<>();

		
			stack.push("4");
			stack.push("16");
			stack.push("28");
			stack.push("78");
			stack.push("90"); 

		
			System.out.println("Push Stack: " + stack);
			
		}
	}



